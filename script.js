// Language switching logic
document.addEventListener('DOMContentLoaded', () => {
    const langButtons = document.querySelectorAll('.lang-btn');
    const storedLang = localStorage.getItem('selectedLang') || 'ja';

    function applyLanguage(lang) {
        document.querySelectorAll('[data-lang-key]').forEach(element => {
            const key = element.getAttribute('data-lang-key');
            if (translations[lang] && translations[lang][key]) {
                // Check if the element is an input or textarea to set placeholder
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translations[lang][key];
                } else {
                    element.innerHTML = translations[lang][key];
                }
            }
        });
        // Update document title and meta description
        document.title = translations[lang].siteTitle;
        document.querySelector('meta[name=\"description\"]').setAttribute('content', translations[lang].siteDescription);

        langButtons.forEach(button => {
            if (button.dataset.lang === lang) {
                button.classList.add('active');
            } else {
                button.classList.remove('active');
            }
        });
        localStorage.setItem('selectedLang', lang);
    }

    langButtons.forEach(button => {
        button.addEventListener('click', () => {
            applyLanguage(button.dataset.lang);
        });
    });

    applyLanguage(storedLang);

    // Cookie Banner Logic
    const cookieBanner = document.getElementById('cookie-banner');
    const cookieAcceptBtn = document.getElementById('cookie-accept-btn');
    const hasAcceptedCookies = localStorage.getItem('acceptedCookies');

    if (!hasAcceptedCookies) {
        setTimeout(() => {
            cookieBanner.classList.add('show');
        }, 1000);
    }

    cookieAcceptBtn.addEventListener('click', () => {
        localStorage.setItem('acceptedCookies', 'true');
        cookieBanner.classList.remove('show');
    });

    // Scroll Animation Logic
    const fadeInElements = document.querySelectorAll('.fade-in-element');

    const observerOptions = {
        root: null, // viewport
        rootMargin: '0px',
        threshold: 0.1 // 10% of the element must be visible
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target); // Stop observing once visible
            }
        });
    }, observerOptions);

    fadeInElements.forEach(element => {
        observer.observe(element);
    });
});
